package interviewQ.Basic_JavaPrograms;

public class Concatenation {
    public static void main(String[] args) {
        System.out.println(1+2+3+"Hi");
        System.out.println(1+2+"Hi"+3+4);
        System.out.println(1+2+"Hi"+(3+4));
        System.out.println("Hi"+1+2+3);
        System.out.println(""+1+2+3+"Hi");
    }
}
